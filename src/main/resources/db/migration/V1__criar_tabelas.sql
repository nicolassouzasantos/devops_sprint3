-- Tabelas
CREATE TABLE PATIO (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  NOME VARCHAR2(255) NOT NULL,
  ENDERECO VARCHAR2(255)
);

CREATE TABLE OPERADOR (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  NOME  VARCHAR2(255) NOT NULL,
  LOGIN VARCHAR2(100) NOT NULL,
  SENHA VARCHAR2(255) NOT NULL,
  PAPEL VARCHAR2(50)  DEFAULT 'ROLE_USER' NOT NULL,
  HABILITADO NUMBER(1) DEFAULT 1 NOT NULL
);

CREATE TABLE AUTOMOVEL (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  PLACA VARCHAR2(20)  NOT NULL,
  CHASSI VARCHAR2(50) NOT NULL,
  TIPO  VARCHAR2(100) NOT NULL,
  COR   VARCHAR2(50),
  LOCALIZACAO_NO_PATIO VARCHAR2(150),
  COMENTARIOS CLOB,
  PATIO_ID NUMBER,
  CONSTRAINT FK_AUTOMOVEL_PATIO FOREIGN KEY (PATIO_ID) REFERENCES PATIO(ID)
);

-- Índices e unicidades
CREATE UNIQUE INDEX UK_OPERADOR_LOGIN  ON OPERADOR (LOGIN);
CREATE UNIQUE INDEX UK_AUTOMOVEL_PLACA ON AUTOMOVEL (PLACA);
CREATE INDEX        IDX_AUTOMOVEL_TIPO ON AUTOMOVEL (TIPO);

